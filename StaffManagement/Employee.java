public class Employee
{		
	String firstName;
	String lastName;
	int age;
	double monthlySalary;
	
	public Employee(String firstName, String lastName, int age, double monthlySalary)
	{
		
			this.firstName = firstName;
			this.lastName = lastName;
			this.age = age;
			this.monthlySalary = monthlySalary;
		
	}

}		